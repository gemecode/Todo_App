import 'package:todo_app/shared/bloc_observer.dart';
import 'package:todo_app/views/home_screen.dart';

void main() {
  Bloc.observer = MyBlocObserver();
  runApp(const MyApp());
}

class MyApp extends StatelessWidget {
  const MyApp({super.key});

